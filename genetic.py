def sort_list(list1, list2): 
  
    zipped_pairs = zip(list2, list1) 
  
    z = [x for _, x in sorted(zipped_pairs)] 
      
    return z 

def test_individuos(individuos, matrix):
    resultados = []
    for i in individuos:
        resultadors = 0
        for a in range(len(i)):
            for b in range(len(i[0])):
                resultadors += (i[a][b] - matrix[a][b])
        resultados.append(resultadors)
    resultados = [abs(i) for i in resultados]


    resultados_sort = sort_list(resultados, list(range(len(resultados))))
    return resultados, resultados_sort




def make_ranking(teste, teste_sort, individuos):
    ranking = []
    for i in range(len(individuos)):

        ranking.append(teste.index(teste_sort[i]))
    return ranking

def shuffle_matrix(m1, m2):
    result = []
    for i in range(len(m1)):
        temp = []
        for j in range(len(m1[0])):
            if random.random() > 0.20:
                temp.append(m1[i][j])
            else:
                temp.append(m2[i][j])
        result.append(temp)
    return result

